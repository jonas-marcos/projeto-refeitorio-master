    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        Fornecedor f = new Fornecedor();
        f.setCnpj(Integer.parseInt(tfCnpj.getText()));
        f.setNome(tfNome.getText());
        f.setEmail(tfEmail.getText());
        f.setTelefone(Integer.parseInt(tfTelefone.getText()));
        FornecedorJpaDAO.getInstance().merge(f);
        limparCampos();
        JOptionPane.showConfirmDialog(rootPane, "Salvo com sucesso !");
    }//GEN-LAST:event_btnSalvarActionPerformed

